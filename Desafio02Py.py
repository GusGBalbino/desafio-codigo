def Sudoku(board):
    for i in range(9):
        linha = {}
        coluna = {}
        block = {}
        linha_quad = 3 * (i//3)
        coluna_quad = 3 * (i%3)
        for j in range(9):
            if board[i][j]!='.' and board[i][j] in linha:
                return False
            linha[board[i][j]] = 1
            if board[j][i]!='.' and board[j][i] in coluna:
                return False
            coluna[board[j][i]] = 1
            lq = linha_quad+j//3
            cq = coluna_quad + j%3
            if board[lq][cq] in block and board[lq][cq]!='.':
                return False
            block[board[lq][cq]]=1
        return True
